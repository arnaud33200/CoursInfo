// Salle.java
public class Salle 
{
    private int ligne;
    private int colonne;
    
    public Salle(int ligne,int colonne)
    {
		this.ligne=ligne;
		this.colonne=colonne;
    } 
    
    public int getLigne()
    {
		return ligne;
    }
    public int getColonne()
    {
		return colonne;
    }
}

public class DrawableCircle extends Circle implements Drawable
{
    protected int color;
    protected double abs;
    protected double ord;

    public DrawableCircle(double r,
			  double abs,double ord)
    {
	super(r);
	this.abs=abs;
	this.ord=ord;
    }
    
    public void setColor(int color)
    {
	this.color=color;
    }
    
    public void setPosition(double x,double y)
    {
	abs=x;
	ord=y;
    }
    
    public void draw()
    {
	System.out.println("DrawableCircle : draw");
    }
}

/*
  implements :

  toutes les méthodes de l'interface doivent
  être implémentées.

  implementer plusieurs interfaces est possible.

 */